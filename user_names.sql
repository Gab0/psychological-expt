CREATE table nomes (
    nome text NOT NULL
);

CREATE table palavras (
    palavra text NOT NULL
);


REVOKE ALL ON nomes FROM anon;
REVOKE ALL ON palavras FROM anon;

CREATE OR REPLACE FUNCTION gen_nickname()
RETURNS TEXT AS $$
DECLARE
    nome_aleatorio TEXT;
    palavra_aleatoria TEXT;
    resultado TEXT;
BEGIN
    -- Seleciona um nome aleatório da tabela nomes
    SELECT nome INTO nome_aleatorio
    FROM nomes
    ORDER BY RANDOM()
    LIMIT 1;

    -- Seleciona uma palavra aleatória da tabela palavras
    SELECT palavra INTO palavra_aleatoria
    FROM palavras
    ORDER BY RANDOM()
    LIMIT 1;

    -- Junta o nome e a palavra com um espaço no meio e coloca em Title Case
    resultado := INITCAP(nome_aleatorio || ' ' || palavra_aleatoria);

    RETURN resultado;
END;
$$ LANGUAGE plpgsql security definer;

REVOKE ALL ON FUNCTION gen_nickname() FROM anon;
GRANT EXECUTE ON FUNCTION gen_nickname() TO anon;


